# Números em Python

# int - inteiro
numero_inteiro = 3
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante =", numero_real)

# Função type() para verificar o tipo de dado
print("Tipo da variavel inteiro =", type(numero_inteiro))
print("Tipo da variavel do numero real =", type(numero_real))
# Tipo float para números reais

# Soma +
soma = 1 + 2
print("1 + 2 =", soma)

# Subtração -
subtracao = 2 - 1
print("2 - 1 =", subtracao)

# Multiplicação *
multiplicacao = 2 * 3
print("2 x 3 =", multiplicacao)

# Divisão /
divisao = 5 / 2
print("10 / 2 =", divisao)
print("Tipo da variavel divisao =", type(divisao))

# Divisão inteira //
divisao_inteira = 5 // 2
print("10 // 2 =", divisao_inteira)
print("Tipo da variavel divisao inteira =", type(divisao_inteira))

# int()
print("Valor em inteiro =", int(divisao))

# float()
print("Valor em float =", float(soma))

# Módulo %
modulo = 10 % 3
print("10 % 3 =", modulo)












